// Operadores Matemáticos

console.log('Operadores: ')

// SOMA
let numero1 = 10;
let numero2 = 20;
let soma = numero1 + numero2;
console.log(`A Soma de ${numero1} e ${numero2} é: ${soma}`);

// SUBTRAÇÃO
let subtracao = numero1 - numero2;
console.log(`A Subtração de ${numero1} e ${numero2} é: ${subtracao}`)

// MULTIPLICAÇÃO
let multiplicacao = numero1 * numero2;
console.log(`A Multiplicação de ${numero1} e ${numero2} é: ${multiplicacao}`)

// DIVISÃO
let divisao = numero1 / numero2;
console.log(`A Divisão de ${numero1} e ${numero2} é: ${divisao}`)

// RESTO
let resto = numero1 % numero2;
console.log(`O Resto da divisão entre ${numero1} e ${numero2} é: ${resto}`)

// INCREMENTO
console.log(`Um incremento de ${numero1} é: ${++numero1}`)

// DECREMENTO
console.log(`Um decremento de ${numero2} é: ${--numero2}`)
